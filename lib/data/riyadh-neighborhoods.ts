export const getNeighborhoods = (): Neighborhood[] => {
    const allNeighborhoods: Neighborhood[] = []

    Object.entries(neighborhoodsList).forEach(([regionKey, names]) => {
        const region = regionKey as keyof typeof neighborhoodsList
        const stats = regionStats[region]

        names.forEach((name) => {
            // Create slight variations to make content less repetitive for SEO
            const variation = Math.floor(Math.random() * 3)
            let customProblem = stats.problem

            if (variation === 1) customProblem = `مشاكل ${name}: ` + stats.problem
            if (variation === 2) customProblem = "نعالج في " + name + ": " + stats.problem

            allNeighborhoods.push({
                id: `${region}-${name.replace(/\s+/g, '-')}`,
                name: `حي ${name}`,
                region: region,
                buildingTypes: stats.types,
                commonProblems: customProblem,
                recommendedInsulation: stats.insulation,
                responseTime: stats.time,
                specialties: stats.specialties
            })
        })
    })

    // Remove duplicates based on name
    return Array.from(new Map(allNeighborhoods.map(item => [item.name, item])).values())
}

export const neighborhoods = getNeighborhoods()
